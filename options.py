parser.add_argument('--test_rgb_root', type=str, default='', help='test rgb image root')
parser.add_argument('--test_depth_root', type=str, default='', help='test thermal image root')
parser.add_argument('--test_gt_root', type=str, default='', help='test gt image root')
parser.add_argument('--save_path', type=str, default='./Results/Result_UMFNet/', help='path to save models and logs')
parser.add_argument('--test_start_epoch', type=int, default=1, help='start evaluation from this epoch')
parser.add_argument('--resume', type=str, default='', help='resume training from checkpoint path')
parser.add_argument('--resume_mode', type=str, default='none', choices=['none', 'resume', 'finetune'], help='resume: restore model and optimizer state; finetune: load model only')
opt = parser.parse_args()
