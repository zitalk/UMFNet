parser.add_argument('--lr_sched', type=str, default='cosine', choices=['cosine', 'step'], help='learning rate schedule')
parser.add_argument('--min_lr', type=float, default=1e-6, help='minimum learning rate for cosine schedule')
parser.add_argument('--warmup_lr', type=float, default=1e-6, help='warmup start learning rate')
parser.add_argument('--warmup_epochs', type=int, default=5, help='number of warmup epochs')
parser.add_argument('--decay_rate', type=float, default=0.1, help='decay rate of learning rate')
